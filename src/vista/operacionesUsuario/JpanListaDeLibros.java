    public void iniciarComponentes(){
        ArrayList<Libro> lsLibros = GestorPrincipal.getGestorLibro().listar();
        
        if(lsLibros.size() == 0){
            JOptionPane.showMessageDialog(this, "AÃºn no hay libros registrados.", "Error", JOptionPane.ERROR_MESSAGE);
        }else{
            Libro libro = null;
            for(int i=0; i<lsLibros.size(); i++){
                libro = lsLibros.get(i);
                jtblLibro.getModel().setValueAt(libro.getIsbn(), i, 0);
                jtblLibro.getModel().setValueAt(libro.getNombre(), i, 1);
                jtblLibro.getModel().setValueAt(libro.getAutor(), i,2);
                
                jtblLibro.getModel().setValueAt(libro.getTipo_de_libro(), i, 3);
            }        
        }
    }
