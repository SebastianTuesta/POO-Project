    public void iniciarComponentes(){
        ArrayList<Libro> lsLibrosprestado = GestorPrincipal.getGestorLibro().listarPrestados();
        
        if(lsLibrosprestado.size() == 0){
            JOptionPane.showMessageDialog(this, "AÃºn no hay libros registrados.", "Error", JOptionPane.ERROR_MESSAGE);
        }else{
            Libro libro = null;
            for(int i=0; i<lsLibrosprestado.size(); i++){
                libro = lsLibrosprestado.get(i);
                jTable1.getModel().setValueAt(libro.getIsbn(), i, 0);
                jTable1.getModel().setValueAt(libro.getNombre(), i, 1);
                jTable1.getModel().setValueAt(libro.getAutor(), i,2);
                jTable1.getModel().setValueAt(libro.getTipo_de_libro(), i, 3);
            }
            }
        }
