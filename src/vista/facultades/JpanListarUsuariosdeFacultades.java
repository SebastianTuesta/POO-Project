     public void iniciarComponentes(){
        ArrayList<Facultad> lsFacultades = GestorPrincipal.getGestorFacultad().listar();
        
        if(lsFacultades.size() == 0){
            JOptionPane.showMessageDialog(this, "AÃºn no hay facultades registradas.", "Error", JOptionPane.ERROR_MESSAGE);
        }else{
            Facultad facultad = null;
            for(int i=0; i<lsFacultades.size(); i++){
                facultad = lsFacultades.get(i);
                jTable1.getModel().setValueAt(facultad.getNombre(), i, 0);
                jTable1.getModel().setValueAt(facultad.getLsUsuarios(), i, 1);
               
            }        
        }
    }
    
    
