    public void iniciarComponentes() {
        ArrayList<Usuario> lsUsuarios = GestorPrincipal.getGestorUsuario().listar();

        if (lsUsuarios.size() == 0) {
            JOptionPane.showMessageDialog(this, "AÃºn no hay usuarios registrados.", "Error", JOptionPane.ERROR_MESSAGE);
        } else {
            Usuario usuario = null;
            for (int i = 0; i < lsUsuarios.size(); i++) {
                usuario = lsUsuarios.get(i);
                jTable1.getModel().setValueAt(usuario.getCodigo(), i, 0);
                jTable1.getModel().setValueAt(usuario.getNombre(), i, 1);
                jTable1.getModel().setValueAt(usuario.getDni(), i, 2);
                jTable1.getModel().setValueAt(usuario.getAp_paterno(), i, 3);
                jTable1.getModel().setValueAt(usuario.getAp_materno(), i, 4);
            }
        }
    }

